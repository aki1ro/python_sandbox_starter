# String slices, same as numbers, just indexing the strings and slicing based on 
# choice of range.

string_to_slice = "slicemeplease"

print(string_to_slice[1:5])
print(string_to_slice[:5])
print(string_to_slice[-1])
print(string_to_slice[0:-2])
print(string_to_slice[::2])
print(string_to_slice[1::2])
